    static let loc = Location.newDelhi

    private func check(_ y: Int, _ m: Int, _ d: Int,
                       _ expTithi: Int, _ expMasa: Int, _ expAdhika: Int, _ expSaka: Int,
                       file: StaticString = #filePath, line: UInt = #line) {
        let ti = Self.tithi.tithiAtSunrise(year: y, month: m, day: d, loc: Self.loc)
        let mi = Self.masa.masaForDate(year: y, month: m, day: d, loc: Self.loc)
        let label = "\(y)-\(m)-\(d)"
        XCTAssertEqual(ti.tithiNum, expTithi, "tithi \(label)", file: file, line: line)
        XCTAssertEqual(mi.name.rawValue, expMasa, "masa \(label)", file: file, line: line)
        XCTAssertEqual(mi.isAdhika, expAdhika != 0, "adhika \(label)", file: file, line: line)
        XCTAssertEqual(mi.yearSaka, expSaka, "saka \(label)", file: file, line: line)
    }

