#[test]
fn test_solar_edge_cases() {
    let mut eph = Ephemeris::new();
    let delhi = Location::NEW_DELHI;

    let mut failures = 0;

    for (i, &(gy, gm, gd, cal, exp_month, exp_day, exp_year)) in EDGE_CASES.iter().enumerate() {
        let sd = solar::gregorian_to_solar(&mut eph, gy, gm, gd, &delhi, cal);

        if sd.month != exp_month {
            eprintln!("FAIL [{}] {:?} {:04}-{:02}-{:02} month: got {}, expected {}",
                     i, cal, gy, gm, gd, sd.month, exp_month);
            failures += 1;
        }
        if sd.day != exp_day {
            eprintln!("FAIL [{}] {:?} {:04}-{:02}-{:02} day: got {}, expected {}",
                     i, cal, gy, gm, gd, sd.day, exp_day);
            failures += 1;
        }
        if sd.year != exp_year {
            eprintln!("FAIL [{}] {:?} {:04}-{:02}-{:02} year: got {}, expected {}",
                     i, cal, gy, gm, gd, sd.year, exp_year);
            failures += 1;
        }
    }

    eprintln!("Solar edge: {} entries checked, {} failures", EDGE_CASES.len(), failures);
    assert_eq!(EDGE_CASES.len(), 400, "Expected 400 edge cases");
    assert_eq!(failures, 0, "Solar edge: {} failures", failures);
}
